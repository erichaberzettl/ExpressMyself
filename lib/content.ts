function getExpressionDedupKey(entry: ExpressionEntry): string {
  return `${entry.language}:${entry.expression}`
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^\p{L}\p{N}]+/gu, " ")
    .trim();
}

function dedupeExpressions(entries: ExpressionEntry[]): ExpressionEntry[] {
  const seen = new Set<string>();

  return entries.filter((entry) => {
    const key = getExpressionDedupKey(entry);

    if (seen.has(key)) {
      return false;
    }

    seen.add(key);
    return true;
  });
}

export const rawExpressions: ExpressionEntry[] = getPublicExpressions([
  ...dedupeExpressions([...curatedExpressions, ...importedExpressions])
]);

export const expressions: ExpressionEntry[] = rawExpressions.map(normalizeExpressionEntry);
